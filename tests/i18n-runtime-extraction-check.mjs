import fs from 'node:fs';
import path from 'node:path';
import vm from 'node:vm';
import {fileURLToPath} from 'node:url';
const root=path.resolve(path.dirname(fileURLToPath(import.meta.url)),'..');
const checks=[];
const check=(value,name)=>{if(!value)throw new Error(name);checks.push(name)};
const engine=fs.readFileSync(path.join(root,'modules/core/i18n.js'),'utf8');
const enSource=fs.readFileSync(path.join(root,'modules/core/i18n-en.js'),'utf8');
const kuSource=fs.readFileSync(path.join(root,'modules/core/i18n-ku.js'),'utf8');
check(Buffer.byteLength(engine)<15000,'runtime-small');
check(!engine.includes("const en={")&&!engine.includes("const ku={"),'runtime-data-free');
check(enSource.includes('registry.en=Object.freeze('),'english-data-owner');
check(kuSource.includes('registry.ku=Object.freeze('),'kurdish-data-owner');
check(!enSource.includes('MutationObserver')&&!kuSource.includes('MutationObserver'),'dictionaries-data-only');
const store=new Map([['alin_language_v110','ar']]);
const html={lang:'ar',dir:'rtl',dataset:{}};
const body={dir:'rtl',classList:{toggle(){}}};
const context={console,localStorage:{getItem:k=>store.get(k)||null,setItem:(k,v)=>store.set(k,String(v))},document:{readyState:'loading',documentElement:html,body,addEventListener(){},createTreeWalker:null},MutationObserver:undefined,CustomEvent:class{constructor(type,options={}){this.type=type;this.detail=options.detail}},NodeFilter:{SHOW_ELEMENT:1,SHOW_TEXT:4},alert(){},confirm(){return true},prompt(){return null},addEventListener(){},dispatchEvent(){},setTimeout,clearTimeout};
context.window=context;vm.createContext(context);
for(const rel of ['modules/core/i18n-en.js','modules/core/i18n-ku.js','modules/core/i18n.js'])vm.runInContext(fs.readFileSync(path.join(root,rel),'utf8'),context);
check(context.AlinI18n.t('الإعدادات','en')==='Settings','english-runtime');
check(context.AlinI18n.t('الإعدادات','ku')==='ڕێکخستنەکان','kurdish-runtime');
check(Object.keys(context.AlinI18n.dictionaries.en).length===463,'english-deduplicated-count');
check(Object.keys(context.AlinI18n.dictionaries.ku).length===462,'kurdish-deduplicated-count');
check(Object.isFrozen(context.AlinI18n.dictionaries.en)&&Object.isFrozen(context.AlinI18n.dictionaries.ku),'dictionaries-frozen');
const order=JSON.parse(fs.readFileSync(path.join(root,'modules/module-order.json'),'utf8'));
check(order.early.slice(1,4).join('|')==='modules/core/i18n-en.js|modules/core/i18n-ku.js|modules/core/i18n.js','load-order');
console.log(JSON.stringify({ok:true,checks:checks.length,engineBytes:Buffer.byteLength(engine),englishEntries:463,kurdishEntries:462},null,2));
