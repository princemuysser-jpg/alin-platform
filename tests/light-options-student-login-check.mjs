import fs from 'node:fs';
import path from 'node:path';
import {fileURLToPath} from 'node:url';
const root=path.resolve(path.dirname(fileURLToPath(import.meta.url)),'..');
const read=rel=>fs.readFileSync(path.join(root,rel),'utf8');
const options=read('options.js'),mobile=read('store-mobile.html'),desktop=read('store-desktop.html');
const css=read('styles/alin-mobile.css'),student=read('modules/store/student-auth.js');
const checks=[];const check=(v,n)=>{if(!v)throw new Error(n);checks.push(n)};
check(options.includes("const THEME_KEY = 'alin_theme_v234';"),'new-theme-key');
check(options.includes("localStorage.removeItem(LEGACY_THEME_KEY)"),'legacy-dark-cleared');
check(mobile.includes("localStorage.getItem('alin_theme_v234')==='dark'?'dark':'light'"),'mobile-light-first-bootstrap');
check(desktop.includes("localStorage.getItem('alin_theme_v234')==='dark'?'dark':'light'"),'desktop-light-first-bootstrap');
check(!mobile.includes("localStorage.getItem('alin_theme_v110')"),'mobile-no-legacy-theme-bootstrap');
check(mobile.includes('class="mobile-options-icon"'),'mobile-options-svg');
check(css.includes('clear mobile options icon'),'mobile-options-css');
check(student.includes('name="alin_student_phone" type="tel"'),'student-phone-input');
check(student.includes('autocomplete="tel"'),'student-phone-autocomplete');
check(student.includes('data-lpignore="true" data-1p-ignore="true"'),'student-password-manager-ignore');
check(student.includes("raw.includes('@')||/[A-Za-z]/.test(raw)"),'student-email-autofill-rejected');
check(!student.includes('type="email"'),'student-no-email-input');
check(student.includes('يعتمد رقم الهاتف فقط'),'student-phone-only-copy');
console.log(JSON.stringify({ok:true,checks:checks.length,version:'2.3.4'},null,2));
