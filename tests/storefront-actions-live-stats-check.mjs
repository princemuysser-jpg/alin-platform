import fs from 'node:fs';
import path from 'node:path';
import {fileURLToPath} from 'node:url';
const root=path.resolve(path.dirname(fileURLToPath(import.meta.url)),'..');
const desktop=fs.readFileSync(path.join(root,'store-desktop.html'),'utf8');
const mobile=fs.readFileSync(path.join(root,'store-mobile.html'),'utf8');
const desktopCss=fs.readFileSync(path.join(root,'styles/alin-desktop.css'),'utf8');
const mobileCss=fs.readFileSync(path.join(root,'styles/alin-mobile.css'),'utf8');
const sharedCss=fs.readFileSync(path.join(root,'styles/alin-shared.css'),'utf8');
const catalog=fs.readFileSync(path.join(root,'modules/store/discovery-catalog.js'),'utf8');
const core=fs.readFileSync(path.join(root,'modules/store/discovery-core.js'),'utf8');
const discovery=fs.readFileSync(path.join(root,'modules/store/discovery.js'),'utf8');
const options=fs.readFileSync(path.join(root,'options.js'),'utf8');
const checks=[];
const check=(value,name)=>{if(!value)throw new Error(name);checks.push(name)};
check(desktop.includes('data-desktop-control="cart"')&&desktop.includes('<small>السلة</small>'),'desktop-cart-button-preserved');
check(desktopCss.includes('[data-desktop-control="cart"] small'),'desktop-cart-label-only-style');
check(desktopCss.includes('Keep the cart button itself unchanged'),'desktop-cart-button-not-restyled');
check(mobile.includes('class="mobile-cart-icon"><svg viewBox="0 0 24 24">'),'mobile-cart-svg');
check(mobile.includes('M6.2 8h11.6l1 13H5.2l1-13Z'),'mobile-cart-bag-shape');
check(mobileCss.includes('[data-mobile-control="cart"] .mobile-cart-icon svg'),'mobile-cart-svg-style');
check(desktop.includes('class="desktop-notification-icon"')&&mobile.includes('class="mobile-notification-icon"'),'notification-icons-present');
check(desktop.includes('M6.5 9.25a5.5 5.5 0 0 1 11 0'),'desktop-refined-bell');
check(mobile.includes('M6.5 9.25a5.5 5.5 0 0 1 11 0'),'mobile-refined-bell');
for(const html of [desktop,mobile]){
  check(!html.includes('id="alinStatOrders"'),'completed-order-counter-removed');
  check(!html.includes('<small>طلب مكتمل</small>'),'completed-order-label-removed');
  check(!html.includes('id="aboutPlatformBox"'),'about-card-removed-from-store');
  check(!html.includes('id="contactPlatformBox"'),'contact-card-removed-from-store');
  check(!html.includes('class="store-bottom-info"'),'store-bottom-info-removed');
  check((html.match(/id="alinStat(?:Booklets|Teachers|Libraries)"/g)||[]).length===3,'three-store-counters');
}
check(catalog.includes('function renderStoreStats()'),'live-stats-renderer');
check(catalog.includes('alinStatBooklets:booklets')&&catalog.includes('alinStatTeachers:teachers')&&catalog.includes('alinStatLibraries:libraries'),'live-stats-targets');
check(discovery.includes("window.addEventListener('alin:data-refreshed',()=>{ctx.renderStoreStats();ctx.renderStore()})"),'stats-refresh-listener');
check(discovery.includes("['booklets','products','accounts','orders'].includes"),'stats-mutation-listener');
check(desktopCss.includes('grid-template-columns:repeat(3,minmax(0,1fr))')&&mobileCss.includes('grid-template-columns:repeat(3,minmax(0,1fr))'),'three-column-stats-layout');
check(options.includes('id="alinAboutDialog"'),'about-kept-in-options-dialog');
check(options.includes('data-alin-contact')&&options.includes('data-alin-about'),'help-actions-kept-in-options');
check(options.includes('function openAbout()')&&options.includes('function closeAbout()'),'about-dialog-actions');
check(sharedCss.includes('.alin-about-dialog .alin-about-content'),'about-dialog-style');
check(!options.includes("$('#aboutPlatformBox') || $('#storeAbout')"),'about-no-store-scroll');
check(core.includes("$('#mobileBottomCartCount')||$('#mobileCartCount')"),'mobile-cart-count-live-target');
console.log(JSON.stringify({ok:true,checks:checks.length,version:'2.4.1'},null,2));
