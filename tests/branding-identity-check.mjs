import fs from 'node:fs';
const branding=fs.readFileSync('modules/admin/branding.js','utf8');
const css=fs.readFileSync('styles/alin-branding.css','utf8');
const desktop=fs.readFileSync('store-desktop.html','utf8');
const mobile=fs.readFileSync('store-mobile.html','utf8');
const sw=fs.readFileSync('service-worker.js','utf8');
const checks=[];
const check=(condition,name)=>{if(!condition)throw new Error(name);checks.push(name)};
check(!/dataset\.alinTheme\s*=\s*identity/.test(branding),'brand-does-not-overwrite-appearance');
check(branding.includes('dataset.alinBrandTheme'),'separate-brand-theme-dataset');
check(branding.includes("window.addEventListener('alin:theme-changed',applyBrand)"),'reapply-on-appearance-change');
for(const preset of ['alin-original','sky-calm','sage-calm','sand-calm','lavender-calm','rose-calm','graphite-calm'])check(branding.includes(`'${preset}'`),`preset:${preset}`);
for(const token of ['visual_primary','visual_secondary','visual_background','visual_card','platform_logo_path','platform_logo_dark_path','platform_icon_path'])check(branding.includes(token),`save:${token}`);
check(branding.includes("root.className='panel admin-brand-v235'"),'panel-class-preserved');
check(branding.includes('busy=true')&&branding.includes('saveButton.disabled=true'),'double-save-guard');
check(branding.includes('data-clear="logo"')&&branding.includes('data-clear="logoDark"')&&branding.includes('data-clear="icon"'),'asset-clear-controls');
check(css.includes('.ab235-templates')&&css.includes('.ab235-uploads')&&css.includes('@media(max-width:760px)'),'responsive-branding-css');
for(const html of [desktop,mobile])check(html.includes('./styles/alin-branding.css?v=2.3.8'),'branding-css-loaded');
check(sw.includes("'./styles/alin-branding.css'"),'branding-css-cached');
console.log(JSON.stringify({ok:true,checks:checks.length},null,2));
