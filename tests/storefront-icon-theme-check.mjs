import fs from 'node:fs';
import path from 'node:path';
import {fileURLToPath} from 'node:url';
const root=path.resolve(path.dirname(fileURLToPath(import.meta.url)),'..');
const desktop=fs.readFileSync(path.join(root,'store-desktop.html'),'utf8');
const mobile=fs.readFileSync(path.join(root,'store-mobile.html'),'utf8');
const desktopCss=fs.readFileSync(path.join(root,'styles/alin-desktop.css'),'utf8');
const mobileCss=fs.readFileSync(path.join(root,'styles/alin-mobile.css'),'utf8');
const checks=[];
const check=(value,name)=>{if(!value)throw new Error(name);checks.push(name)};
check(desktop.includes('class="desktop-favorite-icon"')&&desktop.includes('data-desktop-control="favorites"'),'desktop-favorite-svg');
check(desktop.includes('class="desktop-notification-icon"')&&desktop.includes('data-desktop-control="notifications"'),'desktop-notification-svg');
check(mobile.includes('class="mobile-favorite-icon"')&&mobile.includes('data-mobile-control="favorites"'),'mobile-favorite-svg');
check(mobile.includes('class="mobile-notification-icon"')&&mobile.includes('aria-label="الإشعارات"'),'mobile-notification-svg');
check(!desktop.includes('class="desktop-heart-icon"'),'desktop-old-heart-removed');
check(!mobile.includes('class="mobile-heart-icon"'),'mobile-old-heart-removed');
check(!desktop.includes('v99-icon-bell'),'desktop-old-bell-removed');
check(!mobile.includes('class="mobile-bell-icon"'),'mobile-old-bell-removed');
check((desktop.match(/<svg viewBox="0 0 24 24">/g)||[]).length>=2,'desktop-inline-icons');
check((mobile.match(/<svg viewBox="0 0 24 24">/g)||[]).length>=2,'mobile-inline-icons');
check(desktopCss.includes('.desktop-favorite-icon')&&desktopCss.includes('.desktop-notification-icon'),'desktop-icon-styles');
check(mobileCss.includes('.mobile-favorite-icon')&&mobileCss.includes('.mobile-notification-icon'),'mobile-icon-styles');
check(mobileCss.includes('.cat-copy strong')&&mobileCss.includes('color:#fff!important'),'mobile-category-white-title');
check(mobileCss.includes('.cat-copy small')&&mobileCss.includes('rgba(255,255,255,.88)'),'mobile-category-white-subtitle');
for(const category of ['booklet','stationery','gift','deal'])check(mobile.includes(`data-v99-category="${category}"`),`mobile-category-${category}`);
console.log(JSON.stringify({ok:true,checks:checks.length,version:'2.4.1'},null,2));
