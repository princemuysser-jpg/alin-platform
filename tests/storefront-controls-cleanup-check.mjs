import fs from 'node:fs';
import path from 'node:path';
import {fileURLToPath} from 'node:url';
const root=path.resolve(path.dirname(fileURLToPath(import.meta.url)),'..');
const desktop=fs.readFileSync(path.join(root,'store-desktop.html'),'utf8');
const mobile=fs.readFileSync(path.join(root,'store-mobile.html'),'utf8');
const mobileCss=fs.readFileSync(path.join(root,'store/mobile-navigation.css'),'utf8');
const checks=[];
const check=(value,name)=>{if(!value)throw new Error(name);checks.push(name)};
check(!desktop.includes('data-desktop-control="filter"'),'desktop-filter-control-removed');
check(!desktop.includes('id="v99DesktopFilterCount"'),'desktop-filter-count-removed');
check(!desktop.includes('class="desktop-filter-backdrop"'),'desktop-filter-drawer-removed');
check(!desktop.includes('id="v99DiscoveryTools"'),'desktop-filter-tools-removed');
check(!desktop.includes('id="studentAuthBtn"'),'desktop-student-login-button-removed');
check(!desktop.includes('class="desktop-account-status"'),'desktop-student-login-status-removed');
check(desktop.includes('showLogin(\'teacher\')')&&desktop.includes('showLogin(\'admin\')'),'desktop-partner-login-kept');
check(!mobile.includes('data-mobile-control="filter"'),'mobile-filter-control-removed');
check(!mobile.includes('id="v99MobileFilterCount"'),'mobile-filter-count-removed');
check(!mobile.includes('class="mobile-filter-backdrop"'),'mobile-filter-drawer-removed');
check(!mobile.includes('id="v99DiscoveryTools"'),'mobile-filter-tools-removed');
check(mobile.includes('data-mobile-control="account"'),'mobile-account-kept');
check(mobile.includes('alinOpenRealAccount()'),'mobile-student-login-kept');
check(mobile.includes('data-mobile-control="cart"')&&mobile.includes('data-mobile-control="favorites"')&&mobile.includes('data-mobile-control="tracking"'),'mobile-primary-actions-kept');
check(mobileCss.includes('grid-template-columns:repeat(4,minmax(0,1fr))!important'),'mobile-nav-four-columns');
console.log(JSON.stringify({ok:true,checks:checks.length,version:'2.3.8'},null,2));
